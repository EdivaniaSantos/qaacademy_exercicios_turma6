package exemplos;

public class MetodosSemRetornoMain {
    public static void main(String[] args) {
        casa(); // chamar o metodo
        apartamento();
        triplex();
        fazenda();
        mansao();
    }
    public static void casa (){
        System.out.println("casa");
    }

    public static void apartamento (){
        System.out.println("apartamento");
    }

    public static void triplex (){
        System.out.println("triplex");
    }

    public static void fazenda (){
        System.out.println("fazenda");
    }

    public static void mansao (){
        System.out.println("mansao");
    }
}

